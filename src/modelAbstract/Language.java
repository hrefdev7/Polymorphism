package modelAbstract;

public abstract class Language {
	/*Classes abstraites : Une classe abstraite est
	une classe qui ne peut pas être instanciée 
	 * et qui peut contenir à la fois des méthodes abstraites 
 et concrètes (implémentées). Les méthodes abstraites sont
 déclarées sans fournir d'implémentation dans la classe abstraite elle-même,
	
et elles doivent être implémentées par toute sous-classe concrète qui étend 
 la classe abstraite.*/
	

	public abstract void displayInfo();
	
}
